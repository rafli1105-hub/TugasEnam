    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {

            Class.forName(driver);
            conn = DriverManager.getConnection(koneksi, user, password);

            String sql = "UPDATE Karyawan SET Nama = ?, Alamat = ?, Gender = ?, Shift = ? WHERE IDKaryawan = ?";
            PreparedStatement pst = conn.prepareStatement(sql);

            pst.setString(1, nama.getText());
            pst.setString(2, ala.getText());
            pst.setString(3, gender.getSelectedItem().toString());
            pst.setString(4, shift.getText());
            pst.setString(5, idkaryawan.getText()); // berdasarkan PK

            int rowsAffected = pst.executeUpdate();

            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Data berhasil diupdate!");
                tampilkanData(); // refresh tabel
            } else {
                JOptionPane.showMessageDialog(this, "ID Karyawan tidak ditemukan!");
            }

            pst.close();
            conn.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal mengupdate data: " + e.getMessage());
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton2ActionPerformed

