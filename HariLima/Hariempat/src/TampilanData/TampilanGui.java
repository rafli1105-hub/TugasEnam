    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed

        try {

            Class.forName(driver);
            conn = DriverManager.getConnection(koneksi, user, password);

            String sql = "INSERT INTO Karyawan (IDKaryawan, Nama, Alamat, Gender, Shift) VALUES (?, ?, ?, ?, ?)";
            PreparedStatement pst = conn.prepareStatement(sql);

            pst.setString(1, idkaryawan.getText());
            pst.setString(2, nama.getText());
            pst.setString(3, ala.getText());
            pst.setString(4, gender.getSelectedItem().toString());
            pst.setString(5, shift.getText());

            pst.executeUpdate();

            JOptionPane.showMessageDialog(this, "Data berhasil disimpan!");

            tampilkanData();

            pst.close();
            conn.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan data: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_simpanActionPerformed

