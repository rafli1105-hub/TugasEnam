    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        try {
            int row = tabel.getSelectedRow();
            if (row >= 0) {

                String id = tabel.getValueAt(row, 0).toString();
                String nama1 = tabel.getValueAt(row, 1).toString();
                String alamat1 = tabel.getValueAt(row, 2).toString();
                String gender1 = tabel.getValueAt(row, 3).toString();
                String shift1 = tabel.getValueAt(row, 4).toString();

                idkaryawan.setText(id);
                nama.setText(nama1);
                ala.setText(alamat1);
                gender.setSelectedItem(gender1);
                shift.setText(shift1);

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error saat memilih data: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_tabelMouseClicked

